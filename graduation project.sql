#13.What is the average time between a customer’s orders?#

select FirstName, LastName, count(orderid) as Total_Orders, avg(Days_Between_Orders) as Days_Between_Orders
from(
	select FirstName, LastName, orderid, datediff(
    ca.date, lag(ca.date) over(partition by c.customerid order by ca.date)
    ) as Days_Between_Orders
    from customers c
    inner join orders o 
    on c.customerid = o.customerid
    inner join calendar ca
    on o.dateid = ca.dateid
    ) as order_intervals
where Days_Between_Orders is not null
group by FirstName, LastName
order by Days_Between_Orders desc;


