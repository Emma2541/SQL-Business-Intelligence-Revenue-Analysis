#23.For each month, what is the percentage of orders that included products from more than one category?#

with Order_Categories as (
    select 
        o.OrderID,
        o.DateID,
        count(distinct p.Categoryid) as Category_Count
    from Orders o
    inner join OrderDetails od 
    on o.OrderID = od.OrderID
	inner join Products p 
    on od.ProductID = p.ProductID
    group by o.OrderID, o.DateID
),
Monthly_Stats as (
    select
        cal.year,
        cal.month,
        date_format(min(cal.Date), '%M') as Month_Name,
        count(distinct oc.OrderID) as Total_Orders,
        sum(case when oc.Category_Count > 1 then 1 else 0 end) as Multi_Category_Orders,
        sum(case when oc.Category_Count = 1 then 1 else 0 end) as Single_Category_Orders
    from Order_Categories oc
    inner join Calendar cal 
    on oc.DateID = cal.DateID
    group by cal.year, cal.month
)
select year, month, Month_Name, Total_Orders, Multi_Category_Orders, Single_Category_Orders,
    round((Multi_Category_Orders / Total_Orders) * 100, 2) as Multi_Category_Percentage,
    round((Single_Category_Orders / Total_Orders) * 100, 2) as Single_Category_Percentage
from Monthly_Stats
order by year, month;