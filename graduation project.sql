#10.How many new customers were acquired each month?#

select ca.year, ca.month, count(distinct c.CustomerID) as New_Customers
from customers c
inner join orders o on c.CustomerID = o.CustomerID
inner join calendar ca on o.DateID = ca.DateID
inner join (
	select 
        o.customerID,
        min(ca.Date) as First_Order_Date
    from orders o
    inner join Calendar ca on o.DateID = ca.DateID
    group by o.CustomerID
) first_order on c.CustomerID = first_order.CustomerID 
    and ca.Date = first_order.First_Order_Date
group by ca.year, ca.month
order by ca.year, ca.month;

