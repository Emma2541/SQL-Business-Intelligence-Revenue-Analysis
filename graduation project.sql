#1.List all customers who have placed more than five orders in the past year.#

select c.customerid, c.firstname, c.lastname, ca.year, count(o.orderid) as Totalorders
from orders o 
inner join customers c
on o.customerid = c.customerid
inner join calendar ca
on ca.dateid = o.dateid
where year= 2024 
group by c.customerid
having count(o.orderid)> 5
order by customerid asc;

