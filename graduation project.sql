#20.Which customers have returned for repeat purchases, and how many times?#

select c.customerid, c.firstname, c.lastname, count(o.orderid) as Total_Orders, min(ca.date) as First_Date_Of_Order, max(ca.date) as Last_Date_Of_Order, Datediff(max(ca.date), min(ca.date)) as Days_In_Between
from customers c
inner join orders o
on c.customerid = o.customerid
inner join calendar ca
on o.dateid = ca.dateid
group by c.customerid, c.firstname, c.lastname
having count(o.orderid) > 1
order by Total_Orders;

#21.What is the monthly sales growth rate over the past year?#

select year, month, Month_Name, Monthly_Sales, Previous_Month_Sales, Sales_Change, 
case
	when Previous_Month_Sales is null then null
    when Previous_Month_Sales = 0 then null
    else round((Sales_Change / Previous_Month_Sales)*100,2)
    End as Growth_Rate_Percent
from(
	select ca.year, ca.month, date_format(min(ca.date), '%M') as Month_Name,
    sum(od.Quantity*od.Unitprice) as Monthly_Sales,
    lag(sum(od.quantity*od.unitprice)) 
		over (order by ca.year, ca.month) as Previous_Month_Sales, 
	sum(od.quantity*od.unitprice) - lag(sum(od.quantity*od.unitprice)) over (order by ca.year, ca.month) as Sales_Change
from orders o
inner join orderdetails od 
on o.orderid = od.orderid
inner join calendar ca
on ca.dateid = o.dateid
group by ca.year, ca.month
)
as Monthly_Sales_Data
order by year, month;


